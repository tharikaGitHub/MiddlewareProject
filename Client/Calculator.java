    private void btnaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaActionPerformed
        // TODO add your handling code here:
        try {
            n1 = Float.parseFloat(txtf.getText());
            n2 = Float.parseFloat(txts.getText());
            op = (String) cbo.getSelectedItem();
            CalStub cs = new CalStub(op);
            switch (op) {
                case "+":
                    ans = cs.add(n1, n2);
                    txta.setText(new Float(ans).toString());
                    break;
                case "-":
                    ans = cs.sub(n1, n2);
                    txta.setText(new Float(ans).toString());
                    break;
                case "*":
                    ans = cs.mul(n1, n2);
                    txta.setText(new Float(ans).toString());
                    break;
                case "/":
                    if (n2 != 0) {
                        ans = cs.div(n1, n2);
                        txta.setText(new Float(ans).toString());
                    } else {
                        JOptionPane.showMessageDialog(this, "Error: You are going to divide by zero. Please enter valid numbers");
                    }
                    break;
            }
        } catch (IOException e) {
        }
    }//GEN-LAST:event_btnaActionPerformed

